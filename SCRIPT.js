// Open Login Modal
function openModal() {
  document.getElementById("loginModal").style.display = "flex";
}

// Close Login Modal
function closeModal() {
  document.getElementById("loginModal").style.display = "flex";
}

// Toggle Mobile Menu
function toggleMenu() {
  var menu = document.getElementById("nav-links");
  menu.style.display = menu.style.display === "block" ? "none" : "block";
}
